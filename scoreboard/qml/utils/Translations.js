// Cache map for O(1) lookup instead of O(n) linear search
var _optionCache = {}
function _buildCache() {
    for (var i = 0; i < languageOptions.length; ++i) {
        var entry = languageOptions[i]
        if (entry && entry.code)
            _optionCache[entry.code] = entry
    }
}
_buildCache()

function optionFor(code) {
    if (code && _optionCache.hasOwnProperty(code))
        return _optionCache[code]
    return languageOptions.length > 0 ? languageOptions[0] : null
}

function labelFor(code) {
    var opt = optionFor(code)
    return opt ? opt.label : ""
}

function flagFor(code) {
    var opt = optionFor(code)
    return opt ? opt.flagSource : ""
}

function localeFor(code) {
    var opt = optionFor(code)
    return opt ? opt.locale : ""
}

function bundleFor(code) {
    if (code && bundles.hasOwnProperty(code))
        return bundles[code]
    return bundles[defaultLanguageCode] || {}
}
